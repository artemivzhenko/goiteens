
inventory = {
    "сахар": {"цена": 50.0, "количество": 10},
    "мука": {"цена": 35.0, "количество": 20},
    "яблочный сок": {"цена": 100.0, "количество": 15},
    "сыр": {"цена": 300.0, "количество": 5},
    "хлеб": {"цена": 25.0, "количество": 50},
    "колбаса": {"цена": 250.0, "количество": 8},
    "масло": {"цена": 200.0, "количество": 10},
    "шоколад": {"цена": 150.0, "количество": 12},
    "печенье": {"цена": 80.0, "количество": 30},
    "крупа": {"цена": 40.0, "количество": 25}
}
while True:
    print("\n1. Добавить товар\n2. Удалить товар\n3. Изменить цену/количество\n4. Показать все товары\n5. Выход")
    choice = input("Выберите опцию: ")
    if choice == "1":
        name = input("Введите название товара: ")
        price = float(input("Введите цену товара: "))
        quantity = int(input("Введите количество: "))
        inventory[name] = {"цена": price, "количество": quantity}
    elif choice == "2":
        name = input("Введите название товара для удаления: ")
        if name in inventory:
            del inventory[name]
            print("Товар удалён.")
        else:
            print("Товар не найден.")
    elif choice == "3":
        name = input("Введите название товара для изменения: ")
        if name in inventory:
            price = float(input("Введите новую цену: "))
            quantity = int(input("Введите новое количество: "))
            inventory[name] = {"цена": price, "количество": quantity}
        else:
            print("Товар не найден.")
    elif choice == "4":
        for name, details in inventory.items():
            print(f"{name}: Цена - {details['цена']}, Количество - {details['количество']}")
    elif choice == "5":
        break
    else:
        print("Неверный выбор.")
